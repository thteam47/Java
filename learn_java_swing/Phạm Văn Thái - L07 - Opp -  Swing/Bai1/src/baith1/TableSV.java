    private void LuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LuuActionPerformed
      if(svmm.isSelected())
      {
        try {
            FileOutputStream fos=new FileOutputStream("svmatma.dat");
            ObjectOutputStream oos=new ObjectOutputStream(fos);
            oos.writeObject(ds);
            
            JOptionPane.showMessageDialog(rootPane, "Luu Thanh cong");
            oos.close();
            fos.close();
        } catch (Exception e) {
            e.printStackTrace();
        }}
      else if (svhttt.isSelected())
      {
          try {
              FileOutputStream fos=new FileOutputStream("svhtn.dat");
              OutputStreamWriter osw=new OutputStreamWriter(fos);
              BufferedWriter bw =new BufferedWriter(osw);
              String line="";
              for(SinhVienHTTT svhtn:ds2)
              {
                  line+=svhtn.getMaSv()+"$";
                  line+=svhtn.getHoTen()+"$";
                  line+=new SimpleDateFormat().format(svhtn.getNgaySinh());
                  line+=svhtn.getGioiTinh();
                  line+=svhtn.getDiemTb();
                  line+=svhtn.getHocPhi();
                  bw.write(line);
                  bw.newLine();
              }
              bw.close();
              fos.close();
              osw.close();
          JOptionPane.showMessageDialog(rootPane, "Luu Sinh vien ht thanh cong");
                  this.setDefaultCloseOperation(EXIT_ON_CLOSE);
          } catch (Exception e) {
          }  // TODO add your handling code here:
    }//GEN-LAST:event_LuuActionPerformed
}
