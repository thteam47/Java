    private void NhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NhapActionPerformed
if (svmm.isSelected())
        {
        SinhVienMatMa svmmn=new SinhVienMatMa();
        svmmn.setMaSv(txtmasv.getText());
        svmmn.setHoTen(txthoten.getText());
        svmmn.setGioiTinh(gtinh.getSelectedItem().toString());
            SimpleDateFormat sdf=new SimpleDateFormat("dd/MM/yyyy");
        try {
            svmmn.setNgaySinh(sdf.parse(txtngaysinh.getText()));
        } catch (ParseException ex) {
            Logger.getLogger(TableSV.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        svmmn.setDiemTb(Float.parseFloat(txtdiemtb.getText()));
        try{
        svmmn.setDonvi(txtdonvi.getText());
        svmmn.setLuong(txtluong.getText());
        ds.add(svmmn);
        for (SinhVienMatMa x :ds)
            {
                x.hienThi();
            }
        }catch(Exception e)
        {
            e.printStackTrace();
        }
            JOptionPane.showMessageDialog(rootPane, "Them thanh cong");
        }else if(svhttt.isSelected())
        {
            SinhVienHTTT svhtn=new SinhVienHTTT();
            svhtn.setMaSv(txtmasv.getText());
            svhtn.setHoTen(txthoten.getText());
            SimpleDateFormat sdf=new SimpleDateFormat("dd/mm/yyyy");
            try {
            svhtn.setNgaySinh(sdf.parse(txtngaysinh.getText()));
            } catch (ParseException ex) {
                Logger.getLogger(TableSV.class.getName()).log(Level.SEVERE, null, ex);
            }
            svhtn.setGioiTinh(gtinh.getSelectedItem().toString());
            svhtn.setHocPhi(txthocphi.getText());
            ds2.add(svhtn);
            for (SinhVienHTTT x :ds2)
            {
                x.hienThi();
            }
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Ban phai chon sinh vien");
                
                }  
    }//GEN-LAST:event_NhapActionPerformed

